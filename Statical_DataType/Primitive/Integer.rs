fn main(){
    //size of i32 is 32 bits and range is -2^31 to 2^31-1
    let x : i32 = -10;
    let y : u32 = 20;
    println!("signed x : {}",x);
    println!("unsigned y : {}",y);
    // i8,i16,i32,i64,i128,isize
    // u8,u16,u32,u64,u128,usize
    // default is i32
    let a = 10;
    let b = 20;
    println!("a : {}",a);
    println!("b : {}",b);
    
}